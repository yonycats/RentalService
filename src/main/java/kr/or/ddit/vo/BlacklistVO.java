package kr.or.ddit.vo;

import java.time.LocalDate;

public class BlacklistVO {

	private String blId; /* * BL_ID 예시 BL000001 */
	private int userNo; /*  */
	private String blContent; /*  */
	private int blCnt; /*  */
	private LocalDate blStart; /*  */
	private LocalDate blEnd; /*  */
	public String getBlId() {
		return blId;
	}
	


	public void setBlId(String blId) {
		this.blId = blId;
	}
	public int getUserNo() {
		return userNo;
	}
	public void setUserNo(int userNo) {
		this.userNo = userNo;
	}
	public String getBlContent() {
		return blContent;
	}
	public void setBlContent(String blContent) {
		this.blContent = blContent;
	}
	public int getBlCnt() {
		return blCnt;
	}
	public void setBlCnt(int blCnt) {
		this.blCnt = blCnt;
	}
	public LocalDate getBlStart() {
		return blStart;
	}
	public void setBlStart(LocalDate blStart) {
		this.blStart = blStart;
	}
	public LocalDate getBlEnd() {
		return blEnd;
	}
	public void setBlEnd(LocalDate blEnd) {
		this.blEnd = blEnd;
	}
	@Override
	public String toString() {
		return "BlacklistVO [blId=" + blId + ", userNo=" + userNo + ", blContent=" + blContent + ", blCnt=" + blCnt
				+ ", blStart=" + blStart + ", blEnd=" + blEnd + "]";
	}
	
	
	
}
